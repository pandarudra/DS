class Solution {
  public:
    vector<int> subarraySum(vector<int> &arr, int target) {
        int left=0,right=0;
        int sum=0;
        int n=arr.size();
        while(left<n && right<n){
            sum+=arr[right];
            while(left<n && sum>target){
                sum-=arr[left];
                left++;
            }
            if(sum==target){
                return {left+1,right+1};
            }
            right++;
        }
        return {-1};
    }
};

